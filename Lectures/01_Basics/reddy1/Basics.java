package yaswanth.reddy1;

public class Basics {
    public static void main(String[] args) {
        //----if condition
        if (true){
            System.out.println("hiiii");
        }
        int a=10;
        if (a!=10){//if TRUE prints
            System.out.println("helooooo");
        }


        //----while loop(when we don't know how many times loop gonna run)
        int i=1;
        while(i<10){
            System.out.println(i);
            i++;
        }

        //----for loop (when we know how many times loop gonna run)
        for(int i1=1;i1<20;i1++){
            System.out.println(i1);
        }
    }
}
